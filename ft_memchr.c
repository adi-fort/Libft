#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	size_t	i;
	char	*s;

	i = 0;
	s = (char *)str;
	while (i < n)
	{
		if (s[i] == c)
		{
			return ((void *)(&s[i]));
		}
		i++;
	}	
	return (NULL);
}
